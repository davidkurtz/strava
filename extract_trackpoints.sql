column grade format 999.99
column avg_dist heading 'AVG|Dist' format 99.99
column avg_climb heading 'AVG|Climb'format 9.99
with a as (
select round(lng,5) lng, round(lat,5) lat, median(ele) ele, count(*) num_samples
from trackpoints
--where activity_id IN (select activity_id from trackpoints group by activity_id having count(*) >= 200)
group by round(lng,5), round(lat,5)
having count(*) >= 10
), b as (
select (level-2)/1e5 delta
from dual connect by level <= 3
), c as (
select strava_pkg.make_point(a1.lng,a1.lat) loc1, a1.ele ele1
     , strava_pkg.make_point(a2.lng,a2.lat) loc2, a2.ele ele2
	 , LEAST(a1.num_samples,a2.num_samples) num_samples
from a a1, a a2
,    b b1, b b2
where a2.lng != a1.lng
and   a2.lat != a1.lat
and   a2.lng = a1.lng + b1.delta
and   a2.lat = a1.lat + b2.delta
and   b2.delta >= -b1.delta
and   b2.delta >= 0
--and   (b1.delta + b2.delta = 1e-5)
), d as (
select sdo_geom.sdo_distance(loc1,loc2,0.005,'unit=m')  dist
,      ABS(ele2-ele1) climb
,      ROUND(LEAST(ele1,ele2)) min_ele
,      num_samples
from   c
)
select min_ele, 100*sum(climb)/sum(dist) grade
, count(*) num_points
, sum(num_samples) num_samples
, avg(climb) avg_climb
, avg(dist) avg_dist
from   d
where  dist <= 10
group by min_ele
having count(*) >= 100
order by min_ele
/

