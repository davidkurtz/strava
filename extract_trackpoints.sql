merge into trackpoints u
using (
with a as (
select p1.activity_id, p1.seq, p1.lat, p1.ele_Diff, p1.dist
     , strava_pkg.make_point(p1.lng,p1.lat) loc1, p1.ele ele1
     , strava_pkg.make_point(p2.lng,p2.lat) loc2, p2.ele ele2
from   trackpoints p1
,      trackpoints p2
where  p1.activity_id = p2.activity_id
and    p1.seq + 1 = p2.seq
and    p1.seq = p2.seq - 1
and    p1.seq < p2.seq
and    (p1.ele_diff IS NULL OR p1.dist IS NULL)
), b as (
select activity_id, seq
,      sdo_geom.sdo_distance(loc1,loc2,0.005,'unit=m') dist
,      ele2-ele1 ele_diff
from   a
)
select * from b) s
on (u.activity_id = s.activity_id
and u.seq = s.seq)
WHEN MATCHED THEN UPDATE
SET u.dist = s.dist
, u.ele_diff = s.ele_diff
/

