--unify city councils to produce county area
--sdo_union is quite slow - takes around 4 minutes for the 3 city/counnties
----------------------------------------------------------------------------------------------------
ROLLBACK;
DECLARE
  l_union MDSYS.SDO_GEOMETRY;
  l_counter INTEGER;
  l_tol NUMBER := 0.001;
BEGIN
  FOR i IN(
    SELECT area_code, county
    , min(area_number)-1 parent_area_number
    ,count(*) num_areas
    from stage_my_areas
    where area_code = 'CTY'
    group by area_code, county
    having count(*)>1
  ) LOOP

    --update children
    UPDATE stage_my_areas
	SET    area_code = 'CITC'
	,      area_level = 6
	,      parent_area_code = i.area_code
	,      parent_area_number = i.parent_area_number
	,      parent_uqid = 'IRL'||i.parent_area_number
	,      matchable = 0
	WHERE  area_code = i.area_code
	AND    county = i.county;

    -- insert parent (ie county rows)
    INSERT INTO stage_my_areas
	(area_code, area_number, uqid, area_level, matchable
	,parent_area_code, parent_area_number, parent_uqid
	,county, name)
	VALUES
	(i.area_code, i.parent_area_number, 'IRL'||i.parent_area_number, 5, 1
	,'SOVC', 1159320877, 'NE1159320877'
	,i.county,i.county);
    
	l_union := NULL;
	l_counter := 0;
	/*need to rectify geometries others union is null`*/
    FOR j IN (
	  SELECT a.* 
	  ,      SDO_CS.MAKE_2D(SDO_UTIL.RECTIFY_GEOMETRY(geom, l_tol)) rect_geom
	  FROM   stage_my_areas a
	  WHERE  area_code = 'CITC'
	  AND    county = i.county
	  AND    parent_area_code = i.area_code
	  AND    parent_area_number = i.parent_area_number
	  ORDER BY area_number
    ) LOOP
      dbms_output.put_line('+'||j.area_code||'.'||j.area_number||':'||j.name||', Area:'||j.area||'KmSq');
	  l_counter := l_counter + 1;
      IF l_counter = 1 THEN
        l_union := j.rect_geom;
      ELSE
        l_union := SDO_GEOM.SDO_UNION(j.rect_geom, l_union, 1);
      END IF;
	  dbms_output.put_line('Area:'||SDO_GEOM.SDO_AREA(l_union, l_tol)/1e6);
	
    END LOOP;
	
    dbms_output.put_line('='||i.area_code||'.'||i.parent_area_number||':'||i.county);
	UPDATE stage_my_areas
	SET    geom = l_union
    ,      num_pts = SDO_UTIL.GETNUMVERTICES(l_union)
    ,      mbr = sdo_geom.sdo_mbr(l_union)
    ,      area = SDO_GEOM.SDO_AREA(l_union, l_tol,'unit=sq_km')
    WHERE  area_code = i.AREA_CODE
	AND    area_number = i.parent_area_number;
  END LOOP;
END;
