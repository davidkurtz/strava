MERGE INTO my_areas u
USING (
select p.area_code, p.area_number, p.num_children, count(c.area_code) new_num_children
from my_areas p
  LEFT OUTER JOIN my_areas c ON c.parent_area_code = p.area_code AND c.parent_area_number = p.area_number
WHERE p.num_children > 0
GROUP BY p.area_code, p.area_number, p.num_children
HAVING count(c.area_code)=0
) s
ON (u.area_code = s.area_code AND u.area_number = s.area_number)
WHEN MATCHED THEN UPDATE
SET u.num_children = s.new_num_children
