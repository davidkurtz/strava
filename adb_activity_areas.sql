CREATE TABLE STRAVA.ACTIVITY_AREAS
(ACTIVITY_ID NUMBER NOT NULL
,AREA_CODE   VARCHAR2(4) NOT NULL
,AREA_NUMBER NUMBER NOT NULL
,GEOM_LENGTH NUMBER
,CONSTRAINT ACTIVITY_AREAS_PK PRIMARY KEY (ACTIVITY_ID, AREA_CODE, AREA_NUMBER)
,CONSTRAINT ACTIVITY_AREAS_FK FOREIGN KEY (ACTIVITY_ID) REFERENCES STRAVA.ACTIVITIES (ACTIVITY_ID) ON DELETE CASCADE
,CONSTRAINT ACTIVITY_AREAS_FK2 FOREIGN KEY (AREA_CODE, AREA_NUMBER) REFERENCES STRAVA.MY_AREAS (AREA_CODE, AREA_NUMBER) ON DELETE CASCADE
);

ALTER TABLE STRAVA.ACTIVITY_AREAS
