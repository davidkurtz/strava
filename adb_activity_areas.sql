ALTER TABLE STRAVA.ACTIVITY_AREAS
ADD CONSTRAINT ACTIVITY_AREAS_FK2 FOREIGN KEY (AREA_CODE, AREA_NUMBER) REFERENCES STRAVA.MY_AREAS (AREA_CODE, AREA_NUMBER)
ON DELETE CASCADE
/

CREATE OR REPLACE TRIGGER strava.activity_areas_delete
BEFORE DELETE ON strava.activity_areas
FOR EACH ROW
DECLARE
  e_mutation EXCEPTION;
  PRAGMA EXCEPTION_INIT(e_mutation,-4091);
BEGIN
  UPDATE ACTIVITIES
  SET    processing_status = 4
  WHERE  activity_id = :old.ACTIVITY_ID
  AND    processing_status BETWEEN 5 and 8;
EXCEPTION
  WHEN e_mutation THEN 
    dbms_output.put_line('Ignoring '||sqlerrm);
END;
/
show errors

